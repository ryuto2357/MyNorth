async function callDeepSeek(message, history, dynamicSystemPrompt) {
  const openai = new OpenAI({
    baseURL: "https://api.deepseek.com",
    apiKey: DEEPSEEK_API_KEY.value(),
  });

  const recentHistory = history.slice(-6);

  const completion = await openai.chat.completions.create({
    model: "deepseek-chat",
    messages: [
      {
        role: "system",
        content: dynamicSystemPrompt,
      },
      ...recentHistory.map((m) => ({
        role: normalizeRole(m.role),
        content: m.text,
      })),
      {
        role: "user",
        content: message,
      },
    ],
    tools: deepseekTools
  });

  const choice = completion.choices[0];
  let finalText = "";
  let graphUpdated = false;

  // --- DEEPSEEK INTERCEPTION LOGIC ---
  if (choice.message.tool_calls && choice.message.tool_calls.length > 0) {
    for (const call of choice.message.tool_calls) {
      const args = JSON.parse(call.function.arguments); 
      const updated = await executeTool(call.function.name, args);
      
      // If any tool succeeds, mark the graph as updated
      if (updated) graphUpdated = true; 
    }
    finalText = choice.message.content || "I've added that to your constellation graph.";
  } else {
    finalText = choice.message.content;
  }

  return { text: finalText, graphUpdated };
}

function normalizeRole(role) {
  if (role === "model" || role === "ai" || role === "assistant") {
    return "assistant";
  }
  if (role === "human" || role === "user") {
    return "user";
  }
  if (role === "system") {
    return "system";
  }
  return "user"; // fallback
}
