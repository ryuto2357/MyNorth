async function executeTool(name, args, userId = "FkNqVdJkVXUKTrErRvD204mNI3q2") {
  console.log(`Executing tool: ${name}`, args);

  try {
    if (name === "create_knowledge_node") {
      const existingNodeSnap = await db.collection('users').doc(userId).collection('nodes')
        .where('label', '==', args.label).limit(1).get();
        
      if (!existingNodeSnap.empty) {
        console.log(`Node '${args.label}' already exists. Skipping duplication.`);
        return true; // Pretend we updated the graph so the AI is happy
      }
      // 1. Save the markdown content to 'files' collection
      const fileRef = await db.collection('files').add({
        user_id: userId,
        filename: `${args.label}.md`,
        content: args.markdown_content,
        created_at: FieldValue.serverTimestamp()
      });

      // 2. Save the node to 'nodes' collection with the file_path link
      const newNode = {
        label: args.label,
        seniority_level: args.seniority_level,
        cluster_id: args.cluster_id,
        status: 'ACTIVE',
        file_path: fileRef.id,
        metadata: { familiarity_score: 0, learning_progress: 0, last_updated: new Date().toISOString() },
        created_at: FieldValue.serverTimestamp()
      };
      await db.collection('users').doc(userId).collection('nodes').add(newNode);
      return true; // Indicates the graph was updated
    }

    if (name === "create_knowledge_link") {
      // Look up the actual Document IDs based on the topic labels the AI provided
      const nodesRef = db.collection('users').doc(userId).collection('nodes');
      const sourceSnap = await nodesRef.where('label', '==', args.source_topic).limit(1).get();
      const targetSnap = await nodesRef.where('label', '==', args.target_topic).limit(1).get();

      if (!sourceSnap.empty && !targetSnap.empty) {
        await db.collection('users').doc(userId).collection('links').add({
          source_id: sourceSnap.docs[0].id,
          target_id: targetSnap.docs[0].id,
          relation_type: args.relation_type,
          strength: args.relation_type === 'PARENT_OF' ? 1.0 : 0.1,
          created_at: FieldValue.serverTimestamp()
        });
        return true;
      }
      console.log("Could not find matching nodes to link.");
      return false;
    }
  } catch (error) {
    console.error("Tool execution failed:", error);
    return false;
  }
}

exports.chatGemini = onRequest({
  cors: true, secrets: [GEMINI_API_KEY, DEEPSEEK_API_KEY], 
  timeoutSeconds: 300,
  memory: "1GB"
}, async (req, res) => {
  const {history = [], message} = req.body;
  if (!message) {
    return res.status(400).send("Message is required");
  }

  const ai = new GoogleGenAI({
    apiKey: GEMINI_API_KEY.value(),
  });

  const classificationPrompt = `
  Classify the user message.

  Rules:
  - SIMPLE: casual chat, short answers, basic explanations
  - COMPLEX: coding, math, multi-step reasoning, long analysis

  Reply with ONLY one word:
  SIMPLE or COMPLEX

  User message:
  "${message}"
  `;

  const classificationResult = await ai.models.generateContent({
    model: "gemini-3-flash-preview",
    contents: classificationPrompt,
  });

  const decision = classificationResult.text.trim().toUpperCase();

  let finalText;
  let graphUpdated;

  const today = new Date().toISOString().split('T');
  const dynamicSystemPrompt = SYSTEM_PROMPT + `\n\nCRITICAL RULE: Today's date is ${today}. You MUST use ${today} whenever you fill out the {{DATE}} or {{TIMESTAMP}} fields in your templates.`;

  if (decision == "COMPLEX") {
    try {
      const dsResult = await callDeepSeek(message, history, dynamicSystemPrompt);
      finalText = dsResult.text;
      graphUpdated = dsResult.graphUpdated;
    } catch (error) {
      res.status(200).json({
        model: "System",
        message: error.message || "Error calling DeepSeek API",
      });
      return;
    }
  } else {
    const recentHistory = history.slice(-6);

    const chat = await ai.chats.create({
      model: "gemini-3-flash-preview",
      history: recentHistory.map((m) => ({
        role: m.role, parts: [{text: m.text}],
      })),
    });

    const response = await chat.sendMessage({
      message: message,
      config: {
        systemInstruction: dynamicSystemPrompt,
        tools: constellationTools
      },
    });

    if (response.functionCalls && response.functionCalls.length > 0) {
      for (const call of response.functionCalls) {
        const updated = await executeTool(call.name, call.args);
        
        if (updated) graphUpdated = true;
      }
      
      // AI might not generate text if it calls a tool, so we provide a fallback
      finalText = response.text || "I have mapped this to your knowledge constellation.";
    } else {
      finalText = response.text;
    }
  }

  res.status(200).json({
    model: "Morgan",
    message: finalText,
    graphUpdated: graphUpdated
  });
});

